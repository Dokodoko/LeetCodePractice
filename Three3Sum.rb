def three_sum(nums)
  nums.sort!
  reshash=Hash.new
  res=Array.new
  for i in 0..nums.size-3
    break if nums[i]>0
    for j in i+1..nums.size-2
      k=0-nums[i]-nums[j]
      break if k<nums[j]
      ki=bisearch(nums, j+1, nums.size, k)
      if ki>0
        t=nums[i], nums[j], k
        if reshash[t]==nil
          res.push t
          reshash[t]=1
        end
      end
    end
  end
  res
end

def bisearch(nums, l, h, target)
  m=(l+h)/2
  return m if target==nums[m]
  if target>nums[m]
    return -1 if m+1==h
    return bisearch(nums, m+1, h, target)
  else
    return -1 if l==m
    return bisearch(nums, l, m, target)
  end
end

